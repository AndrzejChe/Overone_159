# Примените все математические операции, например +,-, **, % и т. д., к самой переменной

x = int(input("Ввведите переменную x: "))
b = int(input("Ввведите число для математической операции: "))
k = x + b
print("сумма =", k)
a = x - b
print("вычитание =", a)
c = x / b
print("деление =", c)
d = x // b
print("получение целой часи от деления =", d)
e = x % b
print("остаток от деления =", e)
f = x * b
print("произведение =", f)
g = x ** b
print("в степени =", g)